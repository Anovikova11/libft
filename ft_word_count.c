#include "libft.h"

int	ft_word_count(char const *s, char c)
{
	int n;
	int i;

	n = 0;
	i = 0;
	while (s[i] != '\0' && s[i] == c)
		i++;
	while (s[i] != '\0')
	{
		if (s[i] == c && s[i - 1] != c)
			n++;
		i++;
		if (s[i] == '\0' && s[i - 1] != c)
			n++;
	}
	return (n);
}
