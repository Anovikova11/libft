#include "libft.h"

int		ft_word_len(char const *s, char c, int i)
{
	int len;

	len = 0;
	while (s[i] == c)
		i++;
	while (s[i] != '\0' && s[i] != c)
	{
		len++;
		i++;
	}
	return (len);
}
